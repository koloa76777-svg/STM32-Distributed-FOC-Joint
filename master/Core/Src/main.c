// 阻塞读 AS5047P 角度, 返回14位原始值
uint16_t AS5047_ReadAngle(void)
{
    uint8_t tx_buf[2] = { (AS5047_CMD_READ_ANGLE >> 8) & 0xFF,
                          (AS5047_CMD_READ_ANGLE)      & 0xFF };
    uint8_t rx_buf[2] = {0};

    HAL_GPIO_WritePin(CSn_GPIO_Port, CSn_Pin, GPIO_PIN_RESET);  // CS拉低选中
    HAL_SPI_TransmitReceive(&hspi1, tx_buf, rx_buf, 2, 100);
    HAL_GPIO_WritePin(CSn_GPIO_Port, CSn_Pin, GPIO_PIN_SET);    // CS拉高

    uint16_t raw = (rx_buf[0] << 8) | rx_buf[1];
    return raw & AS5047_ANGLE_MASK;
}

// 把一个float角度打包成0x101报文发出去
void CAN_SendAngle(float angle)
{
    float_bytes_t conv;
    conv.f = angle;

    TxData[0] = conv.b[0];   // float拆4字节
    TxData[1] = conv.b[1];
    TxData[2] = conv.b[2];
    TxData[3] = conv.b[3];

    TxHeader.StdId = 0x101;          // 标准ID, 和从板滤波器对应
    TxHeader.IDE   = CAN_ID_STD;     // 标准帧
    TxHeader.RTR   = CAN_RTR_DATA;   // 数据帧(不是远程帧)
    TxHeader.DLC   = 4;              // 发4字节(一个float)
    TxHeader.TransmitGlobalTime = DISABLE;

    HAL_CAN_AddTxMessage(&hcan1, &TxHeader, TxData, &TxMailbox);  // 放入邮箱发送
}
